use std::str::FromStr;

#[derive(Debug, Eq, PartialEq, Hash, Copy, Clone)]
enum Tile {
    Ash, Rock
}

impl From<char> for Tile {
    fn from(value: char) -> Self {
        match value {
            '.' => Tile::Ash,
            '#' => Tile::Rock,
            _ => panic!("Bad tile")
        }
    }
}

struct Grid {
    tiles: Vec<Vec<Tile>>
}

impl FromStr for Grid {
    type Err = ();

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        let tiles = s.lines().map(|l| l.chars().map(|ch| ch.into()).collect::<Vec<Tile>>()).collect();
        Ok(Grid { tiles })
    }
}

impl Grid {
    fn rotate(&self) -> Grid {
        let tiles = (0..self.tiles[0].len()).map(|j| (0..self.tiles.len()).map(move |i| self.tiles[i][j]).collect()).collect();
        Grid { tiles }
    }

    fn find_h_axis(&self) -> Option<usize> {
        (0..self.tiles.len()-1)
            .find(|&y| {
                let dy_limit = y.min(self.tiles.len()-y-2);
                // dbg!(self.tiles.len(), y, dy_limit);
                (0..=dy_limit).all(|dy| self.tiles[y-dy] == self.tiles[y+1+dy])
            })
            .map(|y| y+1)
    }

    fn find_h_axis_smudged(&self) -> Option<usize> {
        (0..self.tiles.len()-1)
            .find(|&y| {
                let dy_limit = y.min(self.tiles.len()-y-2);
                // dbg!(self.tiles.len(), y, dy_limit);
                let diff_count: usize = (0..=dy_limit).map(|dy| Self::count_row_diffs(&self.tiles[y-dy], &self.tiles[y+1+dy]))
                    .sum();
                diff_count == 1
            })
            .map(|y| y+1)
    }

    fn count_row_diffs(left: &[Tile], right: &[Tile]) -> usize {
        left.iter()
            .zip(right.iter())
            .filter(|(l, r)| **l != **r)
            .count()
    }
}

fn main() {
    let grids: Vec<Grid> = INPUT.split("\n\n").map(|s| s.parse().expect("Couldn't parse grid")).collect();

    {
        let h_rows: usize = grids.iter().filter_map(|g| g.find_h_axis()).sum();
        let v_cols: usize = grids.iter().map(|g| g.rotate()).filter_map(|g| g.find_h_axis()).sum();

        println!("Part 1: {}", 100 * h_rows + v_cols);
    }

    {
        let h_rows: usize = grids.iter().filter_map(|g| g.find_h_axis_smudged()).sum();
        let v_cols: usize = grids.iter().map(|g| g.rotate()).filter_map(|g| g.find_h_axis_smudged()).sum();

        println!("Part 2: {}", 100 * h_rows + v_cols);
    }

}

